package java_a_beginners_guide.ch01;

public class IfDemo {
    public static void main(String[] args) {
        int myAge = 34;
        int herAge = 42;

        if (myAge == herAge) {
            System.out.println("We are the same age.");
        } else {
            System.out.println("We are not the same age.");
        }
    }
}
